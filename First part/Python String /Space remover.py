text = " my name is Omit Mollah and I am 24 years old. Recently I completed my diploma on Computer Science"


print(text.lstrip()) #Removes whitespace (spaces, tabs, newline) from the beginning (left side) of a string.


print(text.rstrip()) #Removes whitespace from the end (right side) of a string.

print(text.strip()) #Meaning: Removes whitespace from both sides (left and right).
